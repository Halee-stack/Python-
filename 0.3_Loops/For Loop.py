# A for loop in Python is a control-flow statement that executes a block of code once for each item in an iterable object. Unlike some other languages (such as C, Java, or JavaScript), Python's for loop is designed to work directly with collections of data rather than just counting numbers.
# Indentation in Python is not just a style choice—it is part of the language's syntax. Unlike many languages that use braces ({}) to define blocks of code, Python uses indentation (spaces or tabs) to determine which statements belong together.

fruits = ["Apple", "Peach", "Pear"]
for fruit in fruits:
    print(fruit)

    print(fruit + " pie")
print(fruits)
